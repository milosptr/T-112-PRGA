dayInWeek = int(input("Númer dags: "))

if dayInWeek == 1:
  print("Sunnudagur")
elif dayInWeek == 2:
  print("Mánudagur")
elif dayInWeek == 3:
  print("Þriðjudagur")
elif dayInWeek == 4:
  print("Miðvikudagur")
elif dayInWeek == 5:
  print("Fimmtudagur")
elif dayInWeek == 6:
  print("Föstudagur")
elif dayInWeek == 7:
  print("Laugardagur")
else:
  print("Rangur innsláttur")
